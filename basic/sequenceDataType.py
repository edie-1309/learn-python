## List
# mutable -> setiap elemennya dapat diubah
# used for store multiple values in variable
names = ['edie', 'rangga', 'ilham']

# 0 is the first value of items, like array in another programming language
print(names[0])
print(names[:2])

## Tuple
# immutable -> tidak bisa diubah setiap elemennya
coffee = ("Americano", "Latte")
print(coffee)
print(coffee[1])

## Range
x = range(10)
print(x)