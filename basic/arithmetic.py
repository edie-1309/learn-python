# operasi aritmatika, digunakan untuk melakukan operasi matematika dasar
# ( + ) -> penjumlahan
number1 = 4
number2 = 2
resultPenjumlahan = number1 + number2

# ( - ) -> pengurangan
resultPengurangan = number1 - number2

# ( * ) -> perkalian
resultPerkalian = number1 * number2

# ( / ) -> pembagian, hasil pembagian akan selalu bertipe data float
resultPembagianFloat = number1 / number2

#  ( // ) -> pembagian yang bulat, hasil pembagian akan dibulatkan ke bawah
resultPembagianInteger = number1 // number2

# ( % ) -> modulus, sisa hasil dari pembagian
resultModulus = number1 % number2

# ( ** ) -> eksponen/pangkat
resultEksponen = number1 ** number2
print(resultEksponen)